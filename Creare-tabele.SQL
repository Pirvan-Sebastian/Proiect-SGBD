CREATE TABLE f1_seasons (
  season_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_year NUMBER(4) NOT NULL UNIQUE,
  champion_driver_id NUMBER,
  champion_team_id NUMBER,
  CHECK (season_year BETWEEN 1950 AND 2100)
);

CREATE TABLE f1_races (
  race_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id NUMBER NOT NULL REFERENCES f1_seasons(season_id),
  gp_code VARCHAR2(30) NOT NULL UNIQUE,
  gp_name VARCHAR2(120) NOT NULL,
  race_date DATE NOT NULL,
  country VARCHAR2(60),
  city VARCHAR2(60),
  UNIQUE (season_id, gp_name)
);

CREATE TABLE f1_teams (
  team_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  team_code VARCHAR2(10) NOT NULL UNIQUE,
  name VARCHAR2(100) NOT NULL UNIQUE,
  country VARCHAR2(60),
  engine VARCHAR2(60)
);

CREATE TABLE f1_drivers (
  driver_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  driver_code VARCHAR2(10) NOT NULL UNIQUE,
  team_id NUMBER NOT NULL REFERENCES f1_teams(team_id),
  first_name VARCHAR2(60) NOT NULL,
  last_name VARCHAR2(60) NOT NULL,
  country VARCHAR2(60),
  birth_date DATE
);

CREATE TABLE f1_race_results (
  result_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  race_id NUMBER NOT NULL REFERENCES f1_races(race_id),
  driver_id NUMBER NOT NULL REFERENCES f1_drivers(driver_id),
  status VARCHAR2(10) NOT NULL,
  position NUMBER(2),
  points NUMBER(3) DEFAULT 0 NOT NULL,
  fastest_lap CHAR(1) DEFAULT 'N' NOT NULL,
  CHECK (status IN ('FINISH','DNF','DSQ')),
  CHECK (fastest_lap IN ('Y','N')),
  CHECK (points >= 0),
  UNIQUE (race_id, driver_id)
);

CREATE INDEX ix_f1_results_race ON f1_race_results(race_id);
CREATE INDEX ix_f1_results_driver ON f1_race_results(driver_id);
CREATE INDEX ix_f1_drivers_team ON f1_drivers(team_id);